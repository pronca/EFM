package it.eng.care.domain.flow.b2b.model;

public class DrgDTO {

	String drgRegionale;
	Double importoRegionale;
	String onereDegenza;
	String costo;

	public String getDrgRegionale() {
		return drgRegionale;
	}

	public void setDrgRegionale(String drgRegionale) {
		this.drgRegionale = drgRegionale;
	}

	public Double getImportoRegionale() {
		return importoRegionale;
	}

	public void setImportoRegionale(Double importoRegionale) {
		this.importoRegionale = importoRegionale;
	}

	public String getOnereDegenza() {
		return onereDegenza;
	}

	public void setOnereDegenza(String onereDegenza) {
		this.onereDegenza = onereDegenza;
	}

	public String getCosto() {
		return costo;
	}

	public void setCosto(String costo) {
		this.costo = costo;
	}
	

}
